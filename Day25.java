package Day25;
public class Main {
    public static void main(String[] args) {
       //Operator Increment (++) dan Decrement (--)
        // Increment
        int a = 2;
        System.out.println(a++);
        System.out.println(++a);
        
        // Decrement
        int b = 4;
        System.out.println(b--);
        System.out.println(--b);
    }
    
}
