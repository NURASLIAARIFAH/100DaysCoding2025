package Day26;
public class Main {
    public static void main(String[] args) {
        // Operator Penugasan
        int a = 4;
        int b = 2;
        a += b;
        System.out.println(a);
        a -= b;
        System.out.println(a);
        a *= b;
        System.out.println(a);
        a /= b;
        System.out.println(a);
        a %= b;
        System.out.println(a);
    }
    
}
