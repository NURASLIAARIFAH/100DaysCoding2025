package Day9;

public class Main {
    
    public static void main(String[] args) {
        String nama; //Deklarasi variabel adalah proses mendefinisikan variabel dengan menentukan tipe data dan nama variabel
        int umur; 
        
        nama="a'ba"; //Inisialisasi variabel adalah proses memberikan nilai awal kepada variabel yang telah dideklarasikan
        umur=45;
        
        nama="mama"; //Update variabel adalah proses mengubah nilai variabel yang telah ada
        umur=40;
        
        System.out.println(nama);
        System.out.println(umur);
        
        
    }
}
